            się do przepisów celno-dewizowych, bądź też nie został wpuszczony do kraju przez władze
            imigracyjne, przewozi towary, które ze względu na ilość lub rodzaj wymagają dłuższej odprawy
            granicznej i tym samym mogą powodować opóźnienia w odprawie pojazdu na granicy.<br>
            7. Przewoźnik zastrzega, iż nie ponosi on odpowiedzialności z tytułu podania błędnego adresu
            przez Pasażera.<br>
            8. Przewoźnik nie odpowiada za odwołania przewozów z przyczyn od niego niezależnych np.
            zamknięcie granic, ze względu na potrzeby obronności lub bezpieczeństwa państwa bądź w
            wypadku klęski żywiołowej czy ataków terrorystycznych.<br>
            9. Za nie odbycie podróży z winy Przewoźnika Podróżnemu przysługuje zwrot kwoty stanowiącej
            równowartość opłaconej podróży.<br>
            10. Przewoźnik ma prawo do wprowadzenia przesiadek podczas przejazdu ze względów
            logistycznych lub bezpieczeństwa podróżnych.<br>
            11. Przewoźnik oświadcza, iż Pasażer objęty jest ubezpieczeniem od następstw nieszczęśliwych
            wypadków (NNW) Przewoźnika. Ubezpieczenie to obejmuje wyłącznie sytuacje, jakie mogą
            zdarzyć się w trakcie pobytu Pasażera w pojeździe. Po zaistnieniu wypadku szkody należy
            zgłaszać do biura firmy.</p>
            <h2>§ 3. OBOWIĄZKI PASAŻERA</h2>
            <p>1. Pasażer jest zobowiązany posiadać ważny dokument uprawniający go do przekroczenia granicy
            oraz wszelkich innych wymaganych dokumentów i zaświadczeń niezbędnych do odbycia podróży.<br>
            2. Pasażer powinien przybyć (oczekiwać) pod wskazany Przewoźnikowi adres 15 minut przed
            ustaloną przez Przewoźnika godziną wyjazdu, jak również posiadać aktywny numer telefonu podany
            do kontaktu.<br>
            3. Pasażer jest ponadto zobowiązany do złożenia bagażu we wskazane przez kierowcę Przewoźnika
            miejsce, stosowania się do poleceń Kierowcy.<br>
            4. Pasażer jest zobowiązany okazywania, na wezwanie uprawnionych służb granicznych i celnych
            wymaganych dokumentów tj.
            podanie dokładnego adresu, kodu pocztowego, ulicy, nr budynku, miejscowości, kraju, w pisowni
            państwa, którego przewóz dotyczy;<br>
            5. Pasażer jest zobowiązany do zapinania pasów bezpieczeństwa w pojeździe.<br>
            6. Pasażer jest zobowiązany do opisania swojego bagażu poprzez czytelne umieszczenie takich
            danych jak imię, nazwisko i adres zamieszkania.<br>
            7. O ile przepisy kraju docelowego i krajów tranzytowych podróży, nie stanowią inaczej, dzieci do 12
            roku życia muszą podróżować pod opieką osoby dorosłej. W przypadku, gdy dorosły nie jest
            rodzicem bądź prawnym opiekunem, konieczne jest posiadanie przez niepełnoletniego
            „Upoważnienia do przewozu osoby niepełnoletniej” wystawionego i wydanego wraz z biletem przez
            sprzedawcę. Osoby w wieku od 12 do 16 lat mogą podróżować samodzielnie, lecz muszą posiadać
            potwierdzony notarialnie dokument „Upoważnienie do przewozu osoby niepełnoletniej” podpisany
            przez oboje rodziców lub opiekunów prawnych. W szczególnych przypadkach przewoźnik może
            zastosować odstępstwa od zasad opisanych w niniejszym punkcie.<br>
            8. Fakt nieoczekiwania Pasażera pod wskazanym adresem w dniu wyjazdu w określonym przedziale
            czasowym jest równoznaczny z rezygnacją z przejazdu. Przewoźnik ma prawo do żądania od
            Pasażera 100 % zwrotu wartości przejazdu.<br>
            9. W pojeździe obowiązuje całkowity zakaz palenia tytoniu i spożywania napojów alkoholowych.<br>
            10. W trakcie przerw w podróży Pasażer ma obowiązek przestrzegać wyznaczonego uprzednio
            czasu zakończenia przerwy i nie powodować przez swoje zaniedbanie konieczności wszczynania
            poszukiwań bądź opóźnień w odjeździe.<br>
            11. Pasażer jest zobowiązany do przestrzegania przepisów porządkowych obowiązujących w
            transporcie oraz zaleceń kierowcy pojazdu Przewoźnika.<br>
            12. Pasażer odpowiada za wszelkie szkody wyrządzone Przewoźnikowi, szczególnie za zniszczenia
            wyposażenia pojazdu i innym Pasażerom na zasadach ogólnych przewidzianych przepisami
            Kodeksu Cywilnego.<br>
            13. Pasażer, który w sposób umyślny lub wskutek zaniedbania zanieczyszcza pojazd Przewoźnika,
            jest zobowiązany do zapłaty na rzecz Przewoźnika kwoty stanowiącej równowartość kosztów, które
            musi ponieść Przewoźnik w celu naprawienia lub wyczyszczenia pojazdu.<br>
            14. Koszt przejazdu jest ustalany podczas rezerwacji według aktualnego cennika przewozu.</p>
            <h2>§ 4. BAGAŻ</h2>
            <p>1. Pasażer ma prawo do bezpłatnego przewozu maksymalnie 2 sztuk bagażu podstawowego oraz
            jeden bagaż podręczny zgodnie z poniższymi wytycznymi:<br>
            a) dwóch sztuk bagażu podstawowego o wadze do 20 kg każdy, przewożonego w luku bagażowym,<br>
            b) jedna sztuka bagażu podręcznego o wadze do 5 kg.<br>
            2. Bagaż podręczny to bagaż w wymiarach pozwalających na swobodne umieszczenie go pod
            siedzeniem, a jego rozmiary nie powinny utrudniać swobody podróży pozostałym pasażerom.<br>
            3. Przewoźnik ma prawo, odmówić zabrania bagażu, jeżeli nie spełnia on wymogów określonych w
            niniejszym regulaminie. Ma prawo odmówić, także zabrania dodatkowego bagażu w przypadku, gdy
            zabranie go jest niemożliwe i wpływa negatywnie na jakość podróży innych pasażerów.<br>
            4. Nadbagaż powinien być zgłoszony przy rezerwacji biletu, doliczana jest dodatkowa opłata.<br>
            5. Przewoźnik informuje, iż zabrania się przewozu bagażu, jak również posiadania przy sobie
            materiałów i przedmiotów, które mogą zagrażać bezpieczeństwu w transporcie, środków
            niedozwolonych, nieakcyzowanych, niezgodnych z prawem oraz rozporządzeniami.<br>
            6. Przewoźnik nie ponosi odpowiedzialności za bagaże znajdujące się poza lukiem bagażowym oraz
            za bagaż podręczny.<br>
            7. Przewoźnik nie odpowiada za bagaże Pasażerów.<br>
            8. Każda sztuka bagażu przewożona w luku bagażowym powinna być wyraźnie oznakowana przez
            Pasażera trwałą przewieszką z podanym nazwiskiem i adresem. Kierowca może sprawdzić
            zgodność podanych danych z dokumentem tożsamości Pasażera. Bagaż nieopisany i którego nie
            można przypisać do konkretnej osoby, może zostać przez załogę usunięty z pojazdu. W takiej
            sytuacji wykluczone jest dochodzenie roszczeń za utratę bagażu.</p>
            <h2>§ 5. PRZEWÓZ PACZEK</h2>
            <p> 1.Przesyłka, nadana za pośrednictwem naszej Firmy oraz zatwierdzona do przewozu przez
            Przewoźnika, jest odbierana spod wskazanego adresu w danej miejscowości i dostarczana do
            wskazanego miejsca.<br>
            2. Zleceniodawca zgłaszając odbiór przesyłki potwierdza znajomość niniejszego Regulaminu.<br>
            3. Czas dostawy przesyłki wynosi od 2 do 6 dni roboczych.<br>
            4. W przypadku gdy Odbiorca odmówi przyjęcia przesyłki, jest ona zwracana Nadawcy. Koszty
            przewozu (w obie strony) ponosi Nadawca.<br>
            5. W przypadku wykonywania usługi przewozu przesyłek na zlecenie Zleceniodawcy i na jego koszt,
            w razie odmowy uiszczenia należności na rzecz Firmy przez Zleceniodawcę, do jej zapłaty
            zobowiązany jest Nadawca.<br>
            6. Nadawca jest zobowiązany przekazać Przewoźnikowi przesyłkę w stanie umożliwiającym jej
            dostarczenie. Nadawca jest zobowiązany odpowiednio przygotować przesyłkę tzn.:<br>
            a) powinna być zamknięta i zabezpieczona skutecznym środkiem takim jak taśma samoprzylepna, plomby
            czy pieczęcie lakowe,<br>
            b) powinna być odpowiednio wytrzymała,<br>
            c) powinna być opisana gdy wymagają tego właściwości lub zawartość przesyłki, poprzez oznakowanie np.
            "góra/dół" lub "uwaga szkło". W opakowaniu przesyłki nie wolno stosować zabezpieczeń
            mogących uszkodzić tę lub inne przewożone przesyłki<br>
            7. Przewoźnik zastrzega przewożenia paczek nie z godnym z prawem przewozu, typu środki niedozwolone (np. narkotyki itp.), zagrażające życiu, bez akcyzy.<br>
            8. Opakowanie przesyłki wlicza się do jej masy. Cena jest ustalana w trakcie przyjęcia zlecenia.<br>
            9. Każda przesyłka powinna być zaopatrzona w dane Odbiorcy i Nadawcy:<br>
            a) imię i nazwisko<br>
            b) ulicę i numer domu<br>
            c) kod pocztowy<br>
            d) miejscowość<br>
            e) telefon kontaktowy<br>
            10. Za otwarcie przesyłki podczas kontroli przez służby bezpieczeństwa, celne itp. Przewoźnik nie
            odpowiada.<br>
            11. Za wszelkie przewożone rzeczy w paczce odpowiada Nadawca, a także Odbiorca przesyłki.<br>
            12. Przewoźnik nie odpowiada, za rzeczy przewożone w paczce.</p>
            <h2>§ 6. PRZEWÓZ OSÓB ZE ZWIERZĘTAMI</h2>
            <p>1. Przewóz osoby ze zwierzęciem należy zgłosić w chwili rezerwacji, zostanie podjęta decyzja w
            zależności od zwierzaka. Koszt przewozu zwierzaka jest ustalany w chwili rezerwacji.<br>
            2. We wcześniej zgłoszonych przypadkach Przewoźnik może przewieźć zwierzę, tylko pod
            warunkiem jego małych gabarytów, braku zagrożenia i bezpośredniej opieki nad nim Pasażera.
            Przewóz taki należy bezwzględnie zgłosić wcześniej oraz zapewnić transporter dla zwierzaka we
            własnym zakresie.</p>
            <h2>§ 7. UMOWA PRZEWOZU</h2>
            <p>1. Pasażer zobowiązany jest do złożenia rezerwacji u Przewoźnika: telefonicznie, za pomocą strony
            internetowej Przewoźnika, sms, bądź za pośrednictwem komunikatorów
            społecznościowych.<br>
            2. Umowa przewozu zostaje zawarta w momencie przyjęcia rezerwacji telefonicznej lub internetowej
            po potwierdzeniu przez osobę przyjmującą rezerwację informacją zwrotną. Ustne i SMS-owe
            zawarcie umowy przez złożenie rezerwacji jest tak samo wiążące jak zawarcie umowy na piśmie.<br>
            3. Opłatę należy uiścić przed rozpoczęciem podróży gotówką u Kierowcy lub przelewem na konto
            podane przez Przewoźnika.<br>
            4. W momencie złożenia rezerwacji Pasażer akceptuje treść niniejszego regulaminu.<br>
            5. Z uwagi na specyfikę działalności przewoźnika, czyli przewóz osób i mienia od drzwi do drzwi, nie
            istnieje regularny i stały rozkład jazdy. Rozkład jazdy ustalany jest na bieżąco na podstawie
            Rezerwacji dokonywanych na bieżąco. Godziny przekazywane w trakcie rezerwacji są przybliżone.<br>
            6. Dokładna godzina przejazdu zostaje podana Pasażerowi dzień przed wyjazdem.<br>
            7. Rezerwacja potwierdzona przez Pasażera dzień przed wyjazdem do godz. 12:00 obliguje
            opłaceniem niniejszej rezerwacji, pomimo że zrezygnował po potwierdzeniu rezerwacji.<br>
            8. Jeżeli Pasażer nagle zrezygnuje z przejazdu, również jest zobligowany do opłacenia przejazdu.<br>
            9. Przewoźnik może powierzyć wykonanie przewozu innym przewoźnikom, informując o tym fakcie
            Pasażera.<br>
            10. Rezerwacja przewozu jest równoznaczna z zapoznaniem i zaakceptowaniem niniejszego
            regulaminu.</p>
