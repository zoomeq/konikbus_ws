            przypadków losowych, Przewoźnik zobowiązuje się zapewnić transport zastępczy np. u innego
            przewoźnika, zgodnie z zamówieniem.<br>
            2. Przewoźnik zobowiązuje się do przewozu Pasażera i jego bagażu w wyznaczone miejsce,
            maksymalna ilość bagaży w cenie to dwie walizki do 20 kg plus bagaż podręczny do 5 kg.<br>
            3. Dokładne godziny wyjazdu są podawane dzień przed wyjazdem. Z uwagi na specyfikę
            działalności przewoźnika (przewozy Door to Door) godziny wyjazdu i przyjazdu mogą ulec
            zmianie, nie są objęte gwarancją i nie stanowią części umowy przewozu.<br>
            4. Przewoźnik nie ponosi odpowiedzialności za opóźnienia i ich dalsze skutki spowodowane
            czynnikami niezależnymi od niego np. kontrole policyjne, wypadki, korki, problemy techniczne,
            warunki atmosferyczne, utrudnienia w ruchu drogowym lub na przejściach granicznych.<br>
            5. W sytuacjach niezależnych od firmy, awarie itp. Przewoźnik może zmienić miejsca wysadzenia
            adresatów oraz zapewnić dojazd na miejsce posiłkując się innymi firmami przewozowymi, z
            którymi współpracuje.<br>
            6. Przewoźnik jest uprawniony do odmowy wykonania przewozu, lub kontynuacji, bez konieczności
            zwrotu kosztów poniesionych przez Pasażera w związku z podróżą, w przypadku gdy Pasażer
            nie przestrzega warunków umowy przewozu w tym niniejszego Regulaminu:<br>
            a) znajduje się w stanie wskazującym na spożycie alkoholu lub środków odurzających,<br>
            b) zachowuje się w sposób uciążliwy dla współpasażerów, zagrażając bezpieczeństwu i zdrowiu
            współpasażerów,<br>
